"""
Этот код выполняет следующие действия:

Загадывает случайное число от 1 до 100.
Запрашивает у пользователя предположение.
Проверяет предположение пользователя и дает подсказки о том, больше или меньше загаданное число.
Ограничивает количество попыток пользователей и завершает игру, если попытки закончились или число угадано.
"""

import random

def guess_number_game():
    print("Добро пожаловать в игру 'Угадай число'!")
    print("Я загадал число от 1 до 100. Попробуйте угадать его.")
    
    # Генерируем случайное число от 1 до 100
    secret_number = random.randint(1, 100)
    attempts = 0
    max_attempts = 10  # Максимальное количество попыток
    
    while attempts < max_attempts:
        try:
            guess = int(input("Введите ваше предположение: "))
            attempts += 1
            
            if guess < secret_number:
                print("Загаданное число больше вашего предположения.")
            elif guess > secret_number:
                print("Загаданное число меньше вашего предположения.")
            else:
                print(f"Поздравляю! Вы угадали число {secret_number} за {attempts} попыток.")
                break
            
        except ValueError:
            print("Ошибка: введите целое число.")
    
    else:
        print(f"Извините, вы исчерпали все попытки. Загаданное число было {secret_number}.")

if __name__ == "__main__":
    guess_number_game()

